package PBC;

public class SwitchCase {
	public static void main(String[] args) 
	{
		int a=3;
		System.out.println("kodnest");
		switch(a)
		{
		case 1: System.out.println("training");
		break;
		case 2: System.out.println("for freshers");
		break;
		case 3: System.out.println("for intermediate level");
		break;
		case 4: System.out.println("for experts");
		break;
		default : System.out.println("@@@@@@@@@@@@@");
		
		}
		
	}

}
