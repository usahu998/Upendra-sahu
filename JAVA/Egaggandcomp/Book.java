package Egaggandcomp;

public class Book {
    String author;
    int cost;
    public Book(String author,int cost)
    {
    	this.author=author;
    	this.cost=cost;
    }
    public String getAuthor()
    {
    	return author;
    }
    public int getCost()
    {
    	return cost;
    }

}
