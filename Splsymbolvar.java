class Splsymbolvar
{
	public static void main(String args[])
	{
	int temp=10;
	int temp$=20;
	int $temp=30;
	int te$mp=40;
	int temp_=50;
	int _temp=60;
	int te__mp=70;

	System.out.println(temp);
	System.out.println(temp$);
	System.out.println($temp);
	System.out.println(te$mp);
	System.out.println(temp_);
	System.out.println(_temp);
	System.out.println(te__mp);
	}
}